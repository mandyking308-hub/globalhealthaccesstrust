CREATE OR REPLACE FUNCTION public.gift_proposal_create(
  _donor_user_id uuid, _donor_display_name text,
  _related_draft_id uuid, _related_project_id uuid,
  _gift_type public.gift_proposal_gift_type,
  _currency char, _amount_minor bigint, _non_cash_description text,
  _donor_preference_notes text
) RETURNS uuid LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE v_id uuid; v_ref text;
BEGIN
  IF NOT public.can_manage_gift_proposals(auth.uid()) THEN RAISE EXCEPTION 'Not authorised'; END IF;
  v_ref := public.generate_gift_proposal_reference();
  INSERT INTO public.gift_proposals(
    reference, donor_user_id, donor_display_name, related_donation_draft_id, related_project_id,
    gift_type, currency, amount_minor, non_cash_description, donor_preference_notes,
    opened_by, status
  ) VALUES (
    v_ref, _donor_user_id, _donor_display_name, _related_draft_id, _related_project_id,
    _gift_type, COALESCE(_currency,'GBP'), _amount_minor, _non_cash_description,
    _donor_preference_notes, auth.uid(), 'draft'
  ) RETURNING id INTO v_id;
  INSERT INTO public.gift_proposal_events(proposal_id, actor_user_id, event_type, detail)
  VALUES (v_id, auth.uid(), 'created', jsonb_build_object('reference', v_ref, 'gift_type', _gift_type));
  INSERT INTO public.audit_logs(user_id, action, action_type, target_type, target_id, details)
  VALUES (auth.uid(), 'gift_proposal_created', 'insert', 'gift_proposals', v_id,
    jsonb_build_object('reference', v_ref));
  RETURN v_id;
END $$;
