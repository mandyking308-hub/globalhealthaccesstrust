CREATE OR REPLACE FUNCTION public.gift_proposal_enforce_preference_not_binding()
RETURNS trigger LANGUAGE plpgsql SET search_path = public AS $$
BEGIN
  IF NEW.donor_preference_is_binding IS TRUE THEN
    RAISE EXCEPTION 'A donor preference is never a binding restriction. Record the restriction through the trustee decision workflow.';
  END IF;
  RETURN NEW;
END $$;
