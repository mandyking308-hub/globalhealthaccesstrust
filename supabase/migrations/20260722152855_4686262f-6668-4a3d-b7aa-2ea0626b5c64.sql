CREATE OR REPLACE FUNCTION public.restricted_fund_record_change(
  _ledger_id uuid,
  _change_type public.restricted_fund_change_type,
  _delta_amount_minor bigint,
  _reason text,
  _approval_reference text
) RETURNS uuid LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE l public.restricted_funds_ledger%ROWTYPE;
        v_before jsonb; v_after jsonb; v_change uuid;
        v_new_allocated bigint; v_new_committed bigint; v_new_spent bigint;
        v_new_refunded bigint; v_new_reallocated bigint;
        v_new_status public.restricted_fund_status;
BEGIN
  IF NOT public.can_manage_gift_proposals(auth.uid()) THEN RAISE EXCEPTION 'Not authorised'; END IF;
  IF _reason IS NULL OR length(trim(_reason))=0 THEN RAISE EXCEPTION 'Reason required'; END IF;
  IF _delta_amount_minor < 0 THEN RAISE EXCEPTION 'Delta must be non-negative'; END IF;

  SELECT * INTO l FROM public.restricted_funds_ledger WHERE id=_ledger_id FOR UPDATE;
  IF NOT FOUND THEN RAISE EXCEPTION 'Ledger entry not found'; END IF;
  IF l.status IN ('closed','refunded','reallocated') AND _change_type NOT IN ('amend','close') THEN
    RAISE EXCEPTION 'Ledger entry is not modifiable in status %', l.status;
  END IF;

  v_before := jsonb_build_object('original', l.original_amount_minor,'allocated', l.allocated_minor,
    'committed', l.committed_minor,'spent', l.spent_minor,'refunded', l.refunded_minor,
    'reallocated', l.reallocated_minor,'remaining', l.remaining_minor,'status', l.status);

  v_new_allocated := l.allocated_minor; v_new_committed := l.committed_minor;
  v_new_spent := l.spent_minor; v_new_refunded := l.refunded_minor;
  v_new_reallocated := l.reallocated_minor; v_new_status := l.status;

  IF _change_type='allocate' THEN
    IF l.allocated_minor + _delta_amount_minor > l.original_amount_minor THEN RAISE EXCEPTION 'Allocation would exceed original amount'; END IF;
    v_new_allocated := l.allocated_minor + _delta_amount_minor;
  ELSIF _change_type='commit' THEN
    IF l.committed_minor + _delta_amount_minor > l.allocated_minor THEN RAISE EXCEPTION 'Commitment would exceed allocated amount'; END IF;
    v_new_committed := l.committed_minor + _delta_amount_minor;
  ELSIF _change_type='spend' THEN
    IF l.spent_minor + _delta_amount_minor > l.committed_minor THEN RAISE EXCEPTION 'Spend would exceed committed amount'; END IF;
    v_new_spent := l.spent_minor + _delta_amount_minor;
  ELSIF _change_type='refund' THEN
    IF l.spent_minor + l.refunded_minor + l.reallocated_minor + _delta_amount_minor > l.original_amount_minor THEN
      RAISE EXCEPTION 'Refund would exceed remaining balance'; END IF;
    IF _approval_reference IS NULL THEN RAISE EXCEPTION 'Approval reference required for refund'; END IF;
    v_new_refunded := l.refunded_minor + _delta_amount_minor;
  ELSIF _change_type='reallocate' THEN
    IF l.spent_minor + l.refunded_minor + l.reallocated_minor + _delta_amount_minor > l.original_amount_minor THEN
      RAISE EXCEPTION 'Reallocation would exceed remaining balance'; END IF;
    IF _approval_reference IS NULL THEN RAISE EXCEPTION 'Approval reference required for reallocation'; END IF;
    v_new_reallocated := l.reallocated_minor + _delta_amount_minor;
  ELSIF _change_type='close' THEN
    v_new_status := 'closed';
  ELSIF _change_type IN ('amend','approve','open') THEN
    NULL;
  ELSE
    RAISE EXCEPTION 'Unsupported change type %', _change_type;
  END IF;

  IF v_new_status='active' THEN
    IF v_new_refunded > 0 AND (v_new_spent + v_new_refunded + v_new_reallocated) >= l.original_amount_minor THEN
      v_new_status := 'refunded';
    ELSIF v_new_reallocated > 0 AND (v_new_spent + v_new_refunded + v_new_reallocated) >= l.original_amount_minor THEN
      v_new_status := 'reallocated';
    ELSIF v_new_spent >= l.original_amount_minor THEN
      v_new_status := 'fully_spent';
    END IF;
  END IF;

  UPDATE public.restricted_funds_ledger SET
    allocated_minor=v_new_allocated, committed_minor=v_new_committed,
    spent_minor=v_new_spent, refunded_minor=v_new_refunded,
    reallocated_minor=v_new_reallocated, status=v_new_status
  WHERE id=_ledger_id;

  SELECT jsonb_build_object('original', original_amount_minor,'allocated', allocated_minor,
    'committed', committed_minor,'spent', spent_minor,'refunded', refunded_minor,
    'reallocated', reallocated_minor,'remaining', remaining_minor,'status', status)
  INTO v_after FROM public.restricted_funds_ledger WHERE id=_ledger_id;

  INSERT INTO public.restricted_funds_ledger_changes(
    ledger_id, change_type, delta_amount_minor, reason, approval_reference,
    before_snapshot, after_snapshot, actor_user_id
  ) VALUES (
    _ledger_id, _change_type, _delta_amount_minor, _reason, _approval_reference,
    v_before, v_after, auth.uid()
  ) RETURNING id INTO v_change;

  INSERT INTO public.audit_logs(user_id, action, action_type, target_type, target_id, details)
  VALUES (auth.uid(), 'restricted_fund_change', 'update', 'restricted_funds_ledger', _ledger_id,
    jsonb_build_object('type', _change_type, 'delta', _delta_amount_minor));
  RETURN v_change;
END $$;
REVOKE ALL ON FUNCTION public.restricted_fund_record_change(uuid,public.restricted_fund_change_type,bigint,text,text) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.restricted_fund_record_change(uuid,public.restricted_fund_change_type,bigint,text,text) TO authenticated;
