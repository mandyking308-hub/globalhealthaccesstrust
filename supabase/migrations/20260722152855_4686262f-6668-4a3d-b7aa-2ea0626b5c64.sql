CREATE OR REPLACE FUNCTION public.gift_proposal_update_workflow(
  _proposal_id uuid,
  _classification public.gift_proposal_classification,
  _proposed_restriction_wording text,
  _proposed_restriction_purpose text,
  _risk_flags jsonb,
  _due_diligence_notes text,
  _financial_review_notes text,
  _project_feasibility_notes text,
  _status public.gift_proposal_status
) RETURNS void LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE p public.gift_proposals%ROWTYPE;
BEGIN
  IF NOT public.can_manage_gift_proposals(auth.uid()) THEN RAISE EXCEPTION 'Not authorised'; END IF;
  SELECT * INTO p FROM public.gift_proposals WHERE id=_proposal_id FOR UPDATE;
  IF NOT FOUND THEN RAISE EXCEPTION 'Proposal not found'; END IF;
  IF p.status='closed' THEN RAISE EXCEPTION 'Proposal is closed'; END IF;
  UPDATE public.gift_proposals SET
    classification = COALESCE(_classification, classification),
    proposed_restriction_wording = COALESCE(_proposed_restriction_wording, proposed_restriction_wording),
    proposed_restriction_purpose = COALESCE(_proposed_restriction_purpose, proposed_restriction_purpose),
    risk_flags = COALESCE(_risk_flags, risk_flags),
    due_diligence_notes = COALESCE(_due_diligence_notes, due_diligence_notes),
    financial_review_notes = COALESCE(_financial_review_notes, financial_review_notes),
    project_feasibility_notes = COALESCE(_project_feasibility_notes, project_feasibility_notes),
    status = COALESCE(_status, status)
  WHERE id=_proposal_id;
  INSERT INTO public.gift_proposal_events(proposal_id, actor_user_id, event_type, detail)
  VALUES (_proposal_id, auth.uid(), 'classified',
    jsonb_build_object('classification', _classification, 'status', _status));
END $$;
REVOKE ALL ON FUNCTION public.gift_proposal_update_workflow(uuid,public.gift_proposal_classification,text,text,jsonb,text,text,text,public.gift_proposal_status) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.gift_proposal_update_workflow(uuid,public.gift_proposal_classification,text,text,jsonb,text,text,text,public.gift_proposal_status) TO authenticated;

CREATE OR REPLACE FUNCTION public.gift_proposal_trustee_decision(
  _proposal_id uuid,
  _decision public.gift_proposal_decision,
  _decision_reason text,
  _decision_conditions text,
  _accepted_restriction_wording text,
  _accepted_restriction_purpose text,
  _approval_reference text,
  _related_donation_id uuid
) RETURNS uuid LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE p public.gift_proposals%ROWTYPE; v_ledger uuid; v_ref text; v_snapshot jsonb;
BEGIN
  IF NOT public.can_manage_gift_proposals(auth.uid()) THEN RAISE EXCEPTION 'Not authorised'; END IF;
  IF _decision='pending' THEN RAISE EXCEPTION 'Decision required'; END IF;
  IF _decision_reason IS NULL OR length(trim(_decision_reason))=0 THEN RAISE EXCEPTION 'Decision reason required'; END IF;
  SELECT * INTO p FROM public.gift_proposals WHERE id=_proposal_id FOR UPDATE;
  IF NOT FOUND THEN RAISE EXCEPTION 'Proposal not found'; END IF;
  IF p.status='closed' THEN RAISE EXCEPTION 'Proposal is closed'; END IF;

  UPDATE public.gift_proposals SET
    decision=_decision, decision_reason=_decision_reason, decision_conditions=_decision_conditions,
    decided_by=auth.uid(), decided_at=now(),
    related_donation_id = COALESCE(_related_donation_id, p.related_donation_id),
    status = 'decided'
  WHERE id=_proposal_id;

  INSERT INTO public.gift_proposal_events(proposal_id, actor_user_id, event_type, detail)
  VALUES (_proposal_id, auth.uid(), 'decision_recorded',
    jsonb_build_object('decision', _decision, 'reason', _decision_reason));

  IF _decision IN ('accept','accept_with_conditions')
     AND p.classification='restricted'
     AND _accepted_restriction_wording IS NOT NULL AND length(trim(_accepted_restriction_wording))>0
     AND _accepted_restriction_purpose IS NOT NULL AND length(trim(_accepted_restriction_purpose))>0 THEN
    IF _approval_reference IS NULL OR length(trim(_approval_reference))=0 THEN
      RAISE EXCEPTION 'Approval reference required to record a restricted-funds ledger entry';
    END IF;
    IF p.amount_minor IS NULL OR p.amount_minor < 0 THEN
      RAISE EXCEPTION 'A positive amount is required to open a restricted-funds ledger entry';
    END IF;

    UPDATE public.gift_proposals SET
      accepted_restriction_wording=_accepted_restriction_wording,
      accepted_restriction_purpose=_accepted_restriction_purpose,
      accepted_at=now()
    WHERE id=_proposal_id;

    v_ref := public.generate_restricted_fund_reference();
    INSERT INTO public.restricted_funds_ledger(
      reference, gift_proposal_id, donation_id, related_project_id,
      accepted_restriction, purpose, currency, original_amount_minor,
      approval_reference, approved_by
    ) VALUES (
      v_ref, _proposal_id, COALESCE(_related_donation_id, p.related_donation_id), p.related_project_id,
      _accepted_restriction_wording, _accepted_restriction_purpose, p.currency, p.amount_minor,
      _approval_reference, auth.uid()
    ) RETURNING id INTO v_ledger;

    v_snapshot := jsonb_build_object(
      'original', p.amount_minor,'allocated',0,'committed',0,'spent',0,
      'refunded',0,'reallocated',0,'remaining', p.amount_minor,'status','active');
    INSERT INTO public.restricted_funds_ledger_changes(
      ledger_id, change_type, delta_amount_minor, reason, approval_reference,
      before_snapshot, after_snapshot, actor_user_id
    ) VALUES (
      v_ledger, 'open', p.amount_minor,
      'Restricted-funds ledger opened from trustee acceptance',
      _approval_reference,
      jsonb_build_object('original',0,'allocated',0,'committed',0,'spent',0,'refunded',0,'reallocated',0,'remaining',0,'status','active'),
      v_snapshot, auth.uid()
    );
    INSERT INTO public.gift_proposal_events(proposal_id, actor_user_id, event_type, detail)
    VALUES (_proposal_id, auth.uid(), 'restriction_accepted',
      jsonb_build_object('ledger_id', v_ledger, 'reference', v_ref));
  END IF;

  INSERT INTO public.audit_logs(user_id, action, action_type, target_type, target_id, details)
  VALUES (auth.uid(), 'gift_proposal_decided', 'update', 'gift_proposals', _proposal_id,
    jsonb_build_object('decision', _decision, 'ledger_id', v_ledger));
  RETURN v_ledger;
END $$;
