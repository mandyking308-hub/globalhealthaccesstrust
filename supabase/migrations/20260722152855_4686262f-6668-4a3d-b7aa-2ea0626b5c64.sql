# Gift Acceptance and Restricted Funds Policy

**Global Health Access Trust ("GHAT")**
Version 1.0 · Effective 22 July 2026

## 1. Purpose

This policy sets out the framework by which the trustees of Global Health Access Trust decide whether to accept, refuse, suspend, hold, return or reallocate a gift, and how restricted and unrestricted funds are administered and recorded. It exists so that every gift is handled lawfully, consistently with our charitable purposes, and in a way that protects beneficiaries, donors, Project Team members and the Trust.

## 2. Trustee authority

The trustees have exclusive legal authority to accept, refuse, suspend, return or reallocate a gift. Authorised members of Trust Administration may prepare recommendations, but no acceptance, restriction, condition, refund or reallocation is binding on the Trust unless it is recorded through the internal Gift Acceptance workflow and approved in accordance with this policy. Receipt of money or property by GHAT does not amount to acceptance and does not prevent the trustees from reviewing, refusing, returning or reallocating a gift where lawful and appropriate.

## 3. Charitable benefit

Every gift must advance GHAT's charitable purposes and be in the best interests of the Trust and its beneficiaries. Where a proposed gift would not clearly advance those purposes, or would create disproportionate cost, risk, obligation or reputational exposure, the trustees may decline it.

## 4. Acceptable donations

GHAT ordinarily accepts:

- **Cash and electronic payments** in supported currencies through GHAT's approved payment providers.
- **Direct Debit** arrangements set up through GHAT's approved provider under the applicable scheme rules.
- **Bank transfer** to GHAT's designated beneficiary account, with a valid reference, subject to reconciliation.
- **Foreign currency** payments, subject to conversion, cost, sanctions and source-of-funds checks.

No form of payment is accepted automatically. Every route is subject to the checks in this policy.

## 5. Non-cash gifts

Gifts of assets other than money — including securities, property, in-kind goods, services, intellectual property or other assets — are not accepted automatically. Any proposed non-cash gift is subject to separate professional advice proportionate to its value, complexity and risk profile, and to trustee decision. No new class of asset is treated as pre-approved by virtue of GHAT having accepted a similar asset in the past.

## 6. Conditional gifts

A conditional gift is a gift offered subject to a condition affecting whether, how or by whom the funds may be used. Conditions are only binding on GHAT where the trustees expressly accept them in writing through this workflow. Conditions inconsistent with GHAT's charitable purposes, with this policy or with applicable law will not be accepted.

## 7. Anonymous gifts

Anonymous or pseudonymous gifts are subject to the same due-diligence standards as attributed gifts. Where GHAT cannot obtain sufficient information to satisfy those standards, the gift may be refused or returned.

## 8. Third-party payments

Where funds are offered on behalf of another person or entity, or where the payer is not the ultimate source of the funds, GHAT requires sufficient information about the underlying donor and the source of funds. Payments that cannot be adequately explained may be refused, held or returned.

## 9. High-value donations

A donation is treated as high-value when designated as such by the trustees or authorised Trust Administration for reasons including amount, donor profile, restrictions, source of funds, complexity, international features, reputational risk, payment schedule or project dependency. High-value donations are managed under GHAT's separate High-Value Donation Agreement process in addition to this policy.

## 10. Restricted donations

A donation is treated as **restricted** only where GHAT expressly accepts and records a specific restriction in the internal ledger. The accepted wording, purpose, amount, approval and any conditions are recorded before the funds are treated as restricted. A donor's stated preference, hope or expectation — however expressed — is not a legally binding restriction unless the Trust has agreed to it under this policy.

## 11. Unrestricted donations

All other donations are treated as unrestricted and are applied by the trustees to any of GHAT's charitable purposes, subject to the standing 20% operating / 80% project-delivery allocation model. The trustees retain full discretion over the ultimate use of unrestricted funds.

## 12. Donor preferences

Donors may express a preference for a particular project, region or theme. Preferences are recorded, considered and, where feasible, respected, but they do not bind the Trust and do not confer any right of oversight, direction or control over how the funds are used.

## 13. Excessive or impractical restrictions

The trustees may decline to accept a proposed restriction, or accept only part of it, where:

- it is inconsistent with GHAT's charitable purposes;
- it is unlawful or contrary to regulatory requirements;
- it would cause disproportionate cost, complexity or delay;
- it would be impractical to monitor, evidence or report on;
- it would prevent the trustees from meeting their duties; or
- it would confer improper benefit on the donor or a connected person.

## 14. Conflicts of interest

Any actual, potential or perceived conflict of interest concerning a proposed gift — whether involving a trustee, member of Trust Administration, Project Team member or connected person — must be declared and managed under GHAT's Conflicts of Interest procedures before the gift is decided. Conflicted individuals do not participate in the decision.

## 15. Source-of-funds concerns

Where the source of funds is unclear, contested, or reasonably suspected to be linked to unlawful conduct, GHAT will hold, refuse or return the funds and, where applicable, make any required regulatory report. A gift will not be accepted where the trustees are not reasonably satisfied as to the source of funds.

## 16. Sanctions

GHAT will not accept a gift where acceptance would breach applicable sanctions, export control or financial-crime laws.

## 17. Fraud

GHAT will not accept a gift where there are reasonable grounds to suspect that acceptance would facilitate fraud, tax evasion, deception or any other unlawful conduct.

## 18. Reputational risk

The trustees may refuse a gift where acceptance would materially damage the reputation, integrity or beneficiary trust of GHAT, or the safety of Project Team members or beneficiaries. Reputational assessments are recorded and reviewed periodically.

## 19. Donor control

GHAT will not allow a donor, or any person connected to a donor, to control beneficiaries, staff, trustees, Project Team members, suppliers or delivery decisions. Any offer that would in substance confer such control will be refused.

## 20. Private benefit

GHAT will not accept a gift that would confer more than incidental private benefit on a donor or connected person, including improper tax, commercial, promotional or personal advantage.

## 21. Naming and recognition

GHAT may recognise a donor by name, tier or narrative acknowledgement where the donor consents. Recognition does not create control, oversight or approval rights over any project. GHAT reserves the right to remove or refuse recognition where continued recognition would be inappropriate.

## 22. Project failure

A project may not proceed, may pause, may be modified or may be closed for reasons including safety, feasibility, regulatory or partner factors. Project outcomes are never guaranteed. Where a restricted purpose cannot be delivered, the trustees will decide whether to reallocate, hold or return the funds in accordance with this policy and applicable law.

## 23. Reallocation

The trustees may reallocate funds — whether restricted or unrestricted — where doing so is lawful, consistent with the original charitable intent and, in the case of restricted funds, permitted by the accepted restriction, applicable law and any prior donor communication. Reallocation decisions are recorded in the restricted-funds ledger and audit history.

## 24. Return or refund

Refunds are exceptional and considered against operational, legal and regulatory constraints. The trustees may return funds where required by law, where a gift has been refused or where continued acceptance would not be appropriate. Requests for refund are considered under GHAT's Donor and Project Funding Terms and this policy.

## 25. Record keeping

Every gift proposal, acceptance decision, restriction wording, condition, risk assessment, approval, change and refund is recorded internally, with the identity of the deciding officer or trustee, the date and the reason. Restricted funds are additionally tracked in the canonical restricted-funds ledger.

## 26. Decision review

The trustees may review any acceptance, restriction, condition, approval, allocation or refund decision at any time and may vary, suspend or reverse a decision where lawful and appropriate. This includes decisions made in error, decisions made on incomplete information, and decisions where new information later emerges.

## 27. Relationship with other documents

This policy operates alongside GHAT's Terms of Use, Privacy Notice, Cookie Notice, Fundraising Communications framework, Donor and Project Funding Terms and (where designated) High-Value Donation Agreement. Where documents overlap, the more protective standard applies.

## 28. Changes to this policy

This policy is reviewed periodically and updated where necessary. The version and effective date of each release are shown at the top of this page.
