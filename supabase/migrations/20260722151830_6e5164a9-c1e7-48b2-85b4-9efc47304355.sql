  USING (auth.uid() = user_id OR public.is_admin(auth.uid()));
DROP POLICY IF EXISTS "own funding acceptance insert" ON public.donation_funding_terms_acceptances;
CREATE POLICY "own funding acceptance insert" ON public.donation_funding_terms_acceptances FOR INSERT TO authenticated
  WITH CHECK (auth.uid() = user_id);

CREATE OR REPLACE FUNCTION public.accept_donor_project_funding_terms(_draft_id UUID)
RETURNS UUID LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE
  d public.donation_drafts%ROWTYPE;
  doc public.legal_documents%ROWTYPE;
  ver public.legal_document_versions%ROWTYPE;
  existing UUID; v_id UUID;
BEGIN
  IF auth.uid() IS NULL THEN RAISE EXCEPTION 'Sign-in required'; END IF;

  SELECT * INTO d FROM public.donation_drafts WHERE id = _draft_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Draft not found'; END IF;
  IF d.donor_id <> auth.uid() THEN RAISE EXCEPTION 'Not authorised'; END IF;

  SELECT * INTO doc FROM public.legal_documents WHERE slug = 'donor-project-funding-terms';
  IF NOT FOUND OR doc.current_published_version_id IS NULL THEN
    RAISE EXCEPTION 'Donor and Project Funding Terms are not currently published';
  END IF;
  SELECT * INTO ver FROM public.legal_document_versions WHERE id = doc.current_published_version_id;

  SELECT id INTO existing FROM public.donation_funding_terms_acceptances
    WHERE user_id = auth.uid() AND donation_draft_id = _draft_id AND version_id = ver.id;
  IF existing IS NOT NULL THEN RETURN existing; END IF;

  INSERT INTO public.donation_funding_terms_acceptances (
    user_id, document_id, version_id, donation_draft_id,
    amount_minor, currency, frequency,
    operating_allocation_minor, delivery_allocation_minor,
    purpose, proposed_project_id, acceptance_text_snapshot
  ) VALUES (
    auth.uid(), doc.id, ver.id, _draft_id,
    d.amount_minor, d.currency, d.frequency::text,
    d.operating_allocation_minor, d.delivery_allocation_minor,
    d.purpose::text, d.proposed_project_id,
    COALESCE(ver.acceptance_text,
      'I agree to the Donor and Project Funding Terms and confirm the donation details and allocation shown.')
  ) RETURNING id INTO v_id;

  INSERT INTO public.legal_acceptances (
    user_id, document_id, version_id, role,
    acceptance_text_snapshot, event_type, acceptance_context
  ) VALUES (
    auth.uid(), doc.id, ver.id, 'donor',
    ver.acceptance_text, 'acceptance', 'donation:' || _draft_id::text
  )
  ON CONFLICT (user_id, document_id, version_id, event_type, COALESCE(acceptance_context, '')) DO NOTHING;

  INSERT INTO public.audit_logs (user_id, action, action_type, target_type, target_id, details)
  VALUES (auth.uid(), 'donor_project_funding_terms_accepted', 'insert',
    'donation_funding_terms_acceptances', v_id,
    jsonb_build_object('draft_id', _draft_id, 'version_id', ver.id, 'amount_minor', d.amount_minor));

  RETURN v_id;
END $$;

REVOKE ALL ON FUNCTION public.accept_donor_project_funding_terms(UUID) FROM PUBLIC;
