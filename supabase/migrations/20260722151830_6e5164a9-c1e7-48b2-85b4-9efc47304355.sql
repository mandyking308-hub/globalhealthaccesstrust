CREATE TABLE IF NOT EXISTS public.donation_funding_terms_acceptances (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  document_id UUID NOT NULL REFERENCES public.legal_documents(id) ON DELETE RESTRICT,
  version_id UUID NOT NULL REFERENCES public.legal_document_versions(id) ON DELETE RESTRICT,
  donation_draft_id UUID NOT NULL REFERENCES public.donation_drafts(id) ON DELETE CASCADE,
  amount_minor BIGINT NOT NULL,
  currency TEXT NOT NULL,
  frequency TEXT NOT NULL,
  operating_allocation_minor BIGINT NOT NULL,
  delivery_allocation_minor BIGINT NOT NULL,
  purpose TEXT,
  proposed_project_id UUID,
  acceptance_text_snapshot TEXT NOT NULL,
  accepted_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (user_id, donation_draft_id, version_id)
);

